/* 4. Operator Logika 
Operator bitwise merupkan operator yang digunakan untuk operasi berdasarkan bit (biner).
Nama Operator	               Simbol
Logika AND	                    &&
Logika OR	                      ||
Negasi/kebalikan	              !
*/


var a = true;
var b = false;

console.log(a && b); //false
console.log(a || b); //true
console.log(!a); //false

console.log(a && !b); //true