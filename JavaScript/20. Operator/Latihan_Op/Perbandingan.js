/* 3. Operator Perbandingan 
 Opertor yang digunakan untuk membandingkan nilai.
 output berupa bolean (true/false), dapat digunakan ketika menemukan kasus perbandingan

Nama Operator	                  Simbol
Lebih Besar	                       >
Lebih Kecil	                       <
Sama Dengan          	      == atau ===
Tidak Sama dengan	           != atau !==
Lebih Besar Sama dengan	           >=
Lebih Kecil Sama dengan	           <=
*/


var a = 1;
var b = 5;

console.log(a /* 1 */ > b /* 5 */ ); //salah
console.log(a /* 1 */ < b); //benar
console.log(a /* 1 */ === b); //salah
console.log(a /* 1 */ != b); // benar
console.log(a /* 1 */ >= b); //benar
console.log(a /* 1 */ <= b); //benar