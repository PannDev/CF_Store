/* 2. Operator Assignment 
  Operator assignment adalah operator yang digunakan
  untuk memberikan tugas / mengisi nilai variabel.

Nama Operator	               Simbol
Assignment Nilai	                   =
Assignment dan Penambahan	         +=
Assignment dan Pengurangan       	-=
Assignment dan Perkalian	         *=
Assignment dan pangkat       	      **=
Assignment dan Pembagian	         /=
Assignment dan Sisa bagi	         %=
*/


var a = 5; //5
var b = 1; //1    
a += b; //6
console.log(a);

a *= b; //20
console.log(a);

a = a + b;